    private void btnRenomearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRenomearActionPerformed
        // indice do ultimo item selecionado
        int index = lstDeque.getSelectionModel().getMaxSelectionIndex();
        
        String text = JOptionPane.showInputDialog("Renomear deque", mod.getElementAt(index));
        
        if (text != null) {
            String newitem = text.trim();
            if (!newitem.isEmpty()) {
                mod.remove(index);
                mod.add(index, newitem);
                decks.get(index).atualizaArquivo(newitem, index, 1);
                decks.get(index).setNome(newitem);
                lstDeque.getSelectionModel().setLeadSelectionIndex(index);
            }
        }
    }//GEN-LAST:event_btnRenomearActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir?", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION){
            int index = lstDeque.getSelectionModel().getMaxSelectionIndex();
            mod.remove(index);
            decks.get(index).apagarDeck(index); // apagar do arquivo
            decks.remove(index);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcessarActionPerformed
        int index = lstDeque.getSelectionModel().getMaxSelectionIndex();
        TelaCard telaDeck = new TelaCard(decks.get(index));
        telaDeck.setVisible(true);
        decks.set(index, telaDeck.getDeck());
        decks.get(index).setIndiceDoDeckAtual(index);
    }//GEN-LAST:event_btnAcessarActionPerformed

